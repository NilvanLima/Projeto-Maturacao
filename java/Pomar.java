public class Pomar {
    private int codigoPomar;
    private String tipoFruto;
    private String local;

   
    public int getCodigoPomar() {
        return codigoPomar;
    }

    
    public void setCodigoPomar(int codigoPomar) {
        this.codigoPomar = codigoPomar;
    }

    
    public String getTipoFruto() {
        return tipoFruto;
    }

    
    public void setTipoFruto(String tipoFruto) {
        this.tipoFruto = tipoFruto;
    }

    
    public String getLocal() {
        return local;
    }

    
    public void setLocal(String local) {
        this.local = local;
    }
    
}
